"""Symlink-based installer for the vibecrafted iTerm2 / locterm AutoLaunch script.

Run from the host Python env (where vibecrafted_core is importable):

    python -m vibecrafted_core.iterm2_plugin.install_autolaunch
    python -m vibecrafted_core.iterm2_plugin.install_autolaunch --uninstall

The installer:

* Verifies that iTerm2 (or locterm) is installed at the expected app path.
* Creates ``~/Library/Application Support/iTerm2/Scripts/AutoLaunch/`` if
  missing.
* Symlinks ``vc_launcher.py`` into that AutoLaunch directory so iTerm2
  picks it up next boot. We use a symlink — not a copy — so plugin
  upgrades just need a ``pip install -U`` on the host venv.
* Writes a sibling ``uninstall_vc_launcher.sh`` that removes the symlink
  cleanly without touching anything else.
* Prints the next-step user instructions (toggle Python API in iTerm2
  Preferences if needed).

GPL boundary: this installer ships only as part of vibecrafted, drops a
single symlink into iTerm2's AutoLaunch directory, and never modifies
iTerm2 / locterm source. The separation matches the FSF guidance for
"mere aggregation" of GPL'd and non-GPL'd software.
"""

from __future__ import annotations

import argparse
import os
import sys
from pathlib import Path
from textwrap import dedent

AUTOLAUNCH_RELPATH = Path("Library/Application Support/iTerm2/Scripts/AutoLaunch")
LAUNCHER_FILENAME = "vc_launcher.py"
UNINSTALL_SCRIPT = "uninstall_vc_launcher.sh"
ITERM2_APP_CANDIDATES = (
    "/Applications/iTerm.app",
    "/Applications/locterm.app",
    str(Path.home() / "Applications/iTerm.app"),
    str(Path.home() / "Applications/locterm.app"),
)


def autolaunch_dir(home: Path | None = None) -> Path:
    base = home or Path.home()
    return base / AUTOLAUNCH_RELPATH


def launcher_source() -> Path:
    """Path to the bundled vc_launcher.py inside this installed package."""
    return Path(__file__).resolve().parent / LAUNCHER_FILENAME


def find_iterm2_app(candidates: tuple[str, ...] = ITERM2_APP_CANDIDATES) -> str | None:
    for raw in candidates:
        path = Path(raw).expanduser()
        if path.exists():
            return str(path)
    return None


def install(*, home: Path | None = None, force: bool = False) -> Path:
    """Symlink vc_launcher.py into iTerm2's AutoLaunch directory.

    Returns the destination path. Raises ``FileNotFoundError`` if the
    launcher source disappears underneath us, and ``FileExistsError`` if
    the destination already exists and ``force`` is False.
    """
    target_dir = autolaunch_dir(home)
    target_dir.mkdir(parents=True, exist_ok=True)
    target = target_dir / LAUNCHER_FILENAME

    src = launcher_source()
    if not src.exists():
        raise FileNotFoundError(f"vc_launcher.py source missing at {src}")

    if target.exists() or target.is_symlink():
        if not force:
            raise FileExistsError(f"{target} already exists; pass --force to replace")
        target.unlink()

    target.symlink_to(src)
    _write_uninstaller(target_dir, target)
    return target


def _write_uninstaller(target_dir: Path, target: Path) -> Path:
    uninstaller = target_dir / UNINSTALL_SCRIPT
    body = dedent(
        f"""\
        #!/usr/bin/env bash
        # Auto-generated by vibecrafted-core iterm2_plugin installer.
        # Removes the AutoLaunch symlink without touching anything else.
        set -euo pipefail
        if [ -L "{target}" ] || [ -e "{target}" ]; then
          rm -f "{target}"
          echo "removed {target}"
        else
          echo "nothing to remove at {target}"
        fi
        rm -f "$0"
        """
    )
    uninstaller.write_text(body, encoding="utf-8")
    uninstaller.chmod(0o755)
    return uninstaller


def uninstall(*, home: Path | None = None) -> bool:
    target = autolaunch_dir(home) / LAUNCHER_FILENAME
    if target.exists() or target.is_symlink():
        target.unlink()
        return True
    return False


def _print_next_steps(target: Path, iterm2_app: str | None) -> None:
    where = iterm2_app or "iTerm2 (or locterm) not detected in /Applications"
    print(
        dedent(
            f"""
            vibecrafted AutoLaunch installed:
                {target}

            Detected terminal app: {where}

            Next steps (one-time):
              1. Open iTerm2 / locterm.
              2. Preferences → General → Magic → Enable Python API.
              3. Restart iTerm2 / locterm.
              4. The vibecrafted status bar component will appear in
                 Preferences → Profiles → Session → Status Bar → Components.
                 Drag "vibecrafted" onto your status bar to surface
                 live spawn activity.

            To uninstall later, run:
              bash "{autolaunch_dir() / UNINSTALL_SCRIPT}"
            """
        )
    )


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(
        description="Install or remove the vibecrafted iTerm2 / locterm AutoLaunch plugin."
    )
    parser.add_argument(
        "--uninstall",
        action="store_true",
        help="Remove the AutoLaunch symlink instead of installing it.",
    )
    parser.add_argument(
        "--force",
        action="store_true",
        help="Replace an existing AutoLaunch symlink without asking.",
    )
    args = parser.parse_args(argv)

    if args.uninstall:
        removed = uninstall()
        print("removed" if removed else "no AutoLaunch symlink to remove")
        return 0

    try:
        target = install(force=args.force)
    except FileExistsError as exc:
        sys.stderr.write(f"{exc}\n")
        return 1
    except OSError as exc:
        sys.stderr.write(f"install failed: {exc}\n")
        return 1

    _print_next_steps(target, find_iterm2_app())
    if os.environ.get("VIBECRAFTED_DEV") == "1":
        print("[dev] launcher source:", launcher_source())
    return 0


if __name__ == "__main__":  # pragma: no cover - CLI entry point
    raise SystemExit(main())
